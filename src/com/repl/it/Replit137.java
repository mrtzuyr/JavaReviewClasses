package com.repl.it;

public class Replit137 {
	  static int count=0;
	   public void increment(){
	       count++;
	   }
	  public static void main(String[] args) {
		  Replit137 obj1=new Replit137();
		  Replit137 obj2=new Replit137();
		  Replit137 obj3=new Replit137();
	      obj1.increment();
	      obj2.increment();
	      obj3.increment();
	       
	      System.out.println(obj1.count);
	      System.out.println(obj2.count);
	      System.out.println(obj3.count);
	  }
}
