package com.repl.it;

public class Replit137 {
	  static int count=100;
	   static public void increment(){
	       count+=100;
	   }
	  public static void main(String[] args) {
		 
		  System.out.println(count);
		  increment();
		  System.out.println(count);
		  increment();
		  System.out.println(count);
	  }
}
